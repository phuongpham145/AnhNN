    public void fillTable() {
        tbModel.setRowCount(0);
        list.forEach(student -> {
            tbModel.addRow(new String[]{student.getStudentID(), student.getName(), "" + student.getMark(), student.getMajor(), student.getCapacity(), "" + student.isBonus()});
        });
        tbModel.fireTableDataChanged();
    }

