    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblStudents.getSelectedRow();
        if (selectedRow >= 0) {
            String studentID = (String) tblStudents.getValueAt(selectedRow, 0);
            loadStudent(studentID);
        }
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnSortNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortNameActionPerformed
        // TODO add your handling code here:
        Collections.sort(list, new Comparator<Student>(){
        @Override
        public int compare(Student o1, Student o2){
            return o1.getName().compareTo(o2.getName());
        }
        });
        fillTable();
    }//GEN-LAST:event_btnSortNameActionPerformed

    private void btnSortMarkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortMarkActionPerformed
        // TODO add your handling code here:
        Collections.sort(list, (a, b)-> (int)( a.getMark() - b.getMark()));
        fillTable();
    }//GEN-LAST:event_btnSortMarkActionPerformed

    private void loadStudent(String studentID) {
        for (Student student : list) {
            if (studentID.equals(student.getStudentID())) {
                txtStudentID.setText(studentID);
                txtStudentName.setText(student.getName());
                txtMark.setText("" + student.getMark());
                cbxMajor.setSelectedItem(student.getMajor());
                txtAbility.setText(student.getCapacity());
                cbGif.setSelected(student.isBonus());
                break;
            }
        }
    }

