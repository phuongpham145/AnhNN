    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            StringBuilder sb = new StringBuilder();
            Validator.checkEmpty(txtEmployeeId, sb, "Ma nhan vien khong de trong");
            Validator.checkEmpty(txtEmployeeName, sb, "Ten khong de trong");
            Validator.checkAge(txtEmployeeAge, sb);
            Validator.checkSalary(txtEmployeeAmount, sb);
            Validator.checkEmail(txtEmployeeEmail, sb);
            if(sb.length() > 0){
                JOptionPane.showMessageDialog(this, sb.toString(), "Invalid Data", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Employee employee = new Employee();
            employee.setEmployeeId(txtEmployeeId.getText());
            employee.setName(txtEmployeeName.getText());
            employee.setAge(Integer.parseInt(txtEmployeeAge.getText()));
            employee.setEmail(txtEmployeeEmail.getText());
            employee.setSalary(Float.parseFloat(txtEmployeeAmount.getText()));
            if (isEditMode) {
                if (employeeList.findById(employee.getEmployeeId()) == null) {
                    JOptionPane.showConfirmDialog(this, "Empolyee is not exited");
                    return;
                }
                employeeList.update(employee);
            } else {
                if (employeeList.findById(employee.getEmployeeId()) == null) {
                    JOptionPane.showConfirmDialog(this, "Employee is exit");
                    return;
                }
                employeeList.add(employee);
            }
            btnNewActionPerformed(evt);
            employeeList.renderToTable(tblModel);
            JOptionPane.showMessageDialog(this, "Employee saved !");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error : " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

