    private void fillEmployeeToForm(Employee employee) {
        isEditMode = true;
        txtEmployeeName.setText(employee.getName());
        txtEmployeeAge.setText("" + employee.getAge());
        txtEmployeeAmount.setText("" + employee.getSalary());
        txtEmployeeEmail.setText(employee.getEmail());
        txtEmployeeId.setText(employee.getEmployeeId());
    }

